def adjacence(v, n, m):
    adj = []
    if v[0] > 1:
        adj += [(v[0] - 1, v[1])]
    if v[1] > 1:
        adj += [(v[0], v[1] - 1)]
    if v[0] < n:
        adj += [(v[0] + 1, v[1])]
    if v[1] < m:
        adj += [(v[0], v[1] + 1)]
    return adj


def is_end(maze_map, pos):
    return maze_map[pos[0]][pos[1]] == 'F'


def bfs(maze_map, ini):
    if is_end(maze_map, ini):
        return 0
    q = Queue()
    q.put(ini)
    n = len(maze_map)
    m = len(maze_map[0])
    dist = np.array(n * [m * [-1]])
    dist[ini[0]][ini[1]] = 0
    while not q.empty():
        v = q.get()
        adj = adjacence(v, n, m)
        for w in adj:
            if dist[w[0]][w[1]] == -1:
                dist[w[0]][w[1]] = dist[v[0]][v[1]] + 1
                if is_end(maze_map, w):
                    return dist[w[0]][w[1]]
                if maze_map[w[0]][w[1]] == '.':
                    q.put(w)
    return -1


